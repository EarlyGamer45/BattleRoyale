	/* for find the match of every material in every version */
	private final String[] matches;


	private UniversalMaterial(String... entries) {
		this.matches = entries;
	}

    /**
     * @return the correct Material for the version of this bukkit.
     */
	public Material asBukkit() {
		for (String match : matches) {
			if (match != null && Material.valueOf(match) != null) {
				return Material.valueOf(match);
			}
		}
		return null;
	}

    /**
     * @return the correct Material for the version of this bukkit,
     * or the given Material when is not compatible.
     */
	public Material asBukkitSafe(Material def) {
		return ( asBukkit() == null ? def : asBukkit() );
	}

    /**
	 * Match a UniversalMaterial from a bukkit Material. 
     * <p>
     * @return the matched UniversalMaterial or null if not found.
     */
	public static UniversalMaterial getFromBukkit(Material bukkit) {
		for (UniversalMaterial entry : getValues()) {
			for (String match : entry.matches) {
				if (match.equals(bukkit.name())) {
					return entry;
				}	
			}
		}
		return null;
	}

	public static UniversalMaterial[] getValues() {
		Class<?>             clazz = UniversalMaterial.class;
		UniversalMaterial[] values = new UniversalMaterial[clazz.getFields().length];
		try {
			for (int i = 0; i < values.length; i++) {
				Field field = clazz.getFields()[i];
				if (clazz.isAssignableFrom(field.get(clazz).getClass())) {
					values[i] = (UniversalMaterial) field.get(clazz);
				}
			}
		} catch (IllegalArgumentException | IllegalAccessException e) {
			e.printStackTrace();
		}
		return values;
	}
