package com.hotmail.AdrianSR.BattleRoyale.database;

/**
 * Represents the various type 
 * of battle royale stats that 
 * {@link DTBPlayer}'s may have.
 */
public enum StatType {
	
	/**
	 * Battle royale player kills.
	 */
	KILLS,

	/**
	 * The won games of a 
	 * battle royale player.
	 */
	WON_GAMES,

	/**
	 * The lost games of a
	 * battle royale games.
	 */
	LOST_GAMES;
}
